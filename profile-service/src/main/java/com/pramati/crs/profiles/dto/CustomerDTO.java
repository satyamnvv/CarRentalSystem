package com.pramati.crs.profiles.dto;

public class CustomerDTO {

	private Long userId;
	
	private String customerName;
	
	private String customerPhone;
	
	private String customerEmail;
	
	private Integer customerAge;
	
	private String customerAddress;
	
	private String customerPAN;
	
	private String customerAdhaar;
	
	private String customerLiecence;

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public String getCustomerPhone() {
		return customerPhone;
	}

	public void setCustomerPhone(String customerPhone) {
		this.customerPhone = customerPhone;
	}

	public String getCustomerEmail() {
		return customerEmail;
	}

	public void setCustomerEmail(String customerEmail) {
		this.customerEmail = customerEmail;
	}

	public Integer getCustomerAge() {
		return customerAge;
	}

	public void setCustomerAge(Integer customerAge) {
		this.customerAge = customerAge;
	}

	public String getCustomerAddress() {
		return customerAddress;
	}

	public void setCustomerAddress(String customerAddress) {
		this.customerAddress = customerAddress;
	}

	public String getCustomerPAN() {
		return customerPAN;
	}

	public void setCustomerPAN(String customerPAN) {
		this.customerPAN = customerPAN;
	}

	public String getCustomerAdhaar() {
		return customerAdhaar;
	}

	public void setCustomerAdhaar(String customerAdhaar) {
		this.customerAdhaar = customerAdhaar;
	}

	public String getCustomerLiecence() {
		return customerLiecence;
	}

	public void setCustomerLiecence(String customerLiecence) {
		this.customerLiecence = customerLiecence;
	}
	
	
}
